# Looping Statements Demonstration

# 1️⃣ Simple for Loop
for i in range(1, 6):
    print("Number:", i)

# 2️⃣ While Loop Example
count = 1
while count <= 5:
    print("Count:", count)
    count += 1

# 3️⃣ Nested For Loop
for i in range(1, 4):
    for j in range(1, 4):
        print("i =", i, "j =", j)

# 4️⃣ Nested While Loop
i=1
while i <= 3:
    j = 1
    while j <= 3:
        print("i =", i, "j =", j)
        j += 1
    i += 1
