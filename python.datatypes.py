# 1️⃣ NUMERIC DATA TYPES
# Integer
integer_value = 25
# Float
float_value = 10.75
# Complex
complex_value = 3 + 5j
print("---- Numeric Data Types ----")
print("Integer :", integer_value, " Type:", type(integer_value))
print("Float   :", float_value, " Type:", type(float_value))
print("Complex :", complex_value, " Type:", type(complex_value))
print()

# 2️⃣ SEQUENTIAL DATA TYPES
# String
string_value = "Lasya Priya"
# List
list_value = [10, 20, 30, 40]
# Tuple
tuple_value = (1, 2, 3)
print("---- Sequential Data Types ----")
print("String :", string_value, " Type:", type(string_value))
print("List   :", list_value, " Type:", type(list_value))
print("Tuple  :", tuple_value, " Type:", type(tuple_value))
print()

# 3️⃣ DICTIONARY (Mapping Type)
dict_value = {
    "Name": "Lasya",
    "Branch": "ECE",
    "Year": 3
}

print("---- Dictionary ----")
print("Dictionary :", dict_value, " Type:", type(dict_value))
print()

# 4️⃣ SET DATA TYPES
set_value = {1, 2, 3, 3, 4}
print("---- Set ----")
print("Set :", set_value, " Type:", type(set_value))
print()

# 5️⃣ BOOLEAN DATA TYPE
bool_value = True
print("---- Boolean ----")
print("Boolean :", bool_value, " Type:", type(bool_value))
print()

# 6️⃣ NONE DATA TYPE
none_value = None
print("---- None Type ----")
print("None :", none_value, " Type:", type(none_value))
print()
